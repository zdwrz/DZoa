package com.dz.test.activiti;

public class Apple {
	private int price;
	private String color;
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public Apple(int price, String color) {
		super();
		this.price = price;
		this.color = color;
	}
	
}
